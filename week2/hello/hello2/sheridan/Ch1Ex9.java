package sheridan;
//defination of class Ch1Ex9
public class Ch1Ex9
{
    public static void main(String args[])
    {
        //declaration and initialization of variables
        double width=4.5,height=7.9,area,parimeter;
        area=width*height;//calculate area of rectangle
        parimeter=2*(width+height);//calculate parimeter of rectangle
        System.out.println("Area of rectangle : "+area);
        System.out.println("Parimeter of rectangle : "+parimeter);
    }//end of main method
}//end of class  Ch1Ex9